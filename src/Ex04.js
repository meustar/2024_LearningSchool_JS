// 형변환
// 1. 묵시적 형변환
// 2. 명시적 형변환

// 묵시적 형변환
let num1 = "15";
let num2 = 5;

console.log(num1 / num2); // 3
// 자바스크립트 엔진이 나누기 연산을 실행하기 위해
// 문자열 "15"를 => 숫자 15 로 자동변환 (묵시적 형변환)

// 명시적 형변환
console.log(num1 + num2); // 155
// 사직연산중 +(더하기) 연산을 할 경우에는 숫자형을 문자열로 변환.
// 반대로 나머지 사칙연산 (-, *, /)는 문자열을 숫자형으로 변환.

// 직접 내장함수를 이용해 사용자가 직접 형변환.
console.log(parseInt(num1) + num2); // 20
