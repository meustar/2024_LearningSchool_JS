// switch / case

let fruit = "pineapple";

switch (fruit) {
  case "banana":
    console.log("바나나");
    break;
  case "orage":
    console.log("오랜지");
    break;
  case "apple":
    console.log("사과");
    break;
  case "grape":
    console.log("포도");
    break;
  default:
    console.log("다른 과일");
}
